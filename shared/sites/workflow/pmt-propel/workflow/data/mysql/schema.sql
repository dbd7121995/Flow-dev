#-----------------------------------------------------------------------------
#-- PMT_TIKI_SUPPLIER
#-----------------------------------------------------------------------------

DROP TABLE IF EXISTS `PMT_TIKI_SUPPLIER`;


CREATE TABLE `PMT_TIKI_SUPPLIER`
(
	`TIKI_PARTNER_ID` INTEGER(11)  NOT NULL,
	`TIKI_CATEGORY_ID` INTEGER(11)  NOT NULL,
	`DEP_UID` VARCHAR(32),
	`GRP_UID` VARCHAR(32),
	PRIMARY KEY (`TIKI_PARTNER_ID`,`TIKI_CATEGORY_ID`)
)ENGINE=InnoDB  DEFAULT CHARSET='utf8';
